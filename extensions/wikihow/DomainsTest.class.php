		$title = @stripslashes($_GET['title']);
		$action = @$_REQUEST['action'];
		$isTest = false;
		if (!$action || 'view' == $action) {
			$domain = @self::$domainMap[ $title ];
			if ($domain) {
				$isTest = true;
				$wgServer = 'http://' . $domain;
			}
		}
		return $isTest;
	}
}
